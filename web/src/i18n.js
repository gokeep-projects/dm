export function t(key, lang = 'zh') {
  const keys = key.split('.');
  let result = translations[lang];
  for (const k of keys) {
    if (result && typeof result === 'object') {
      result = result[k];
    } else {
      return key;
    }
  }
  return result || key;
}

export function getLang() {
  return localStorage.getItem('dm-lang') || 'zh';
}
